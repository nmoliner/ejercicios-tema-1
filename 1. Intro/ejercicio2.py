a = 10
b = -5
c = "Hola "
d = [1, 2, 3]

print(a * 5)  # Resultado: 50

print(a - b)  # Resultado: 15

print(c + "Mundo")  # Resultado: "Hola Mundo"

print(c * 2)  # Resultado: "Hola Hola "

print(d[-1])  # Resultado: 3

print(d[1:])  # Resultado: [2, 3]

print(d + d)  # Resultado: [1, 2, 3, 1, 2, 3]

